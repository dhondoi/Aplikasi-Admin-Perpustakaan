<div class="row row-cols-1 mb-5">
    <h1 class="col text-dark">Pendahuluan</h1>
    <p class="col text-dark">Junior Web Developer (JWD) merupakan salah satu skema dari program pelatihan Vocational School Graduate Academy (VSGA). Program Vocational School Graduate Academy (VSGA) merupakan program pelatihan dan sertifikasi berbasis kompetensi nasional yang ditujukan bagi lulusan SMK/sederajat serta Diploma 3 dan 4 yang belum bekerja dan memiliki latar belakang pendidikan sesuai dengan skema pelatihan masing-masing. Program VSGA terdiri dari Bimbingan Teknis dan Sertifikasi yang diselenggarakan secara luring dan Pelatihan Intensif dan Sertifikasi yang diselenggarakan secara daring.</p>
    <p class="col text-dark">Diharapkan, dengan terselenggaranya Bimbingan Teknis dan Sertifikasi (Luring) dan Pelatihan Intensif dan Sertifikasi (Daring) Program VSGA dapat meningkatkan kompetensi lulusan Pendidikan Vokasi yang akan berdampak dalam upaya pengurangan angka pengangguran serta mampu memenuhi kebutuhan tenaga terampil di bidang teknologi di Indonesia. Kementerian Komunikasi dan Informasi dalam hal ini berupaya untuk menciptakan ekosistem seimbang untuk memaksimalkan peran triple helix (instansi pemerintahan, sektor privat, dan institusi pendidikan) untuk menjadi fasilitator dan akselerator pendukung ekonomi digital.</p>
    <p class="col text-dark">Aktivitas dalam Bimbingan Teknis dan Sertifikasi (Luring) dan Pelatihan Intensif dan Sertifikasi (Daring) Program VSGA mencakup, diantaranya:
        1. Self-paced Learning (Belajar Mandiri): peserta Pelatihan Intensif dan Sertifikasi (Daring) belajar secara mandiri melalui laptop/komputer, jadwal pelaksanaan Self-paced Learning (Belajar Mandiri) diatur secara mandiri oleh peserta dalam batas durasi pelatihan yang telah ditentukan;
        2. Pertemuan Kelas: sesi pertemuan dengan pengajar secara langsung, peserta Bimbingan Teknis dan Sertifikasi (Luring) belajar secara tatap muka sedangkan Pelatihan Intensif dan Sertifikasi (Daring) belajar melalui live session (webinar);
        3. Hands-on Labs: Peserta akan mengerjakan suatu project baik secara mandiri atau berkelompok;
        4. Evaluasi: peserta akan mengikuti proses evaluasi pelatihan dalam bentuk kuis, ujian, atau menyelesaikan sebuah project;
        5. Program Sertifikasi: program ini diberikan khusus kepada peserta yang memiliki kriteria sesuai dengan ketentuan dan keputusan Panitia Digital Talent Scholarship Kementerian Kominfo; dan
        6. Pasca Pelatihan: peserta Pelatihan Intensif dan Sertifikasi (Daring) yang menyelesaikan pelatihan akan mendapatkan tambahan pelatihan pengembangan soft skills.</p>
